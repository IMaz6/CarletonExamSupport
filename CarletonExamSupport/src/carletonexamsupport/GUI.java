    private void updateDisabling() {
        boolean time = false, date = false, location = false, course = false, section = false;
        if(jCheckBox1.isSelected()) {
            time = true;
            date = true;
            location = true;
        }
        if(jCheckBox2.isSelected()) {
            time = true;
            date = true;
        }
        if(jCheckBox3.isSelected() || jCheckBox4.isSelected()) {
            course = true;
            section = true;
        }
        jList2.setEnabled(course);
        jXDatePicker1.setEnabled(date);
        jComboBox1.setEnabled(time);
        jComboBox2.setEnabled(location);
    }
